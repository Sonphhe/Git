package model;

/**
 *
 * @author Admin
 */
public class Pdetail {
    private int pid;
    private int quantity;
    private Size size;

    public Pdetail() {
    }

    public Pdetail(int pid, int quantity, Size size) {
        this.pid = pid;
        this.quantity = quantity;
        this.size = size;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public Size getSize() {
        return size;
    }

    public void setSize(Size size) {
        this.size = size;
    }

    
   
}
